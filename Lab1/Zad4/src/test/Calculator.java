package test;

public class Calculator {
	double add(double a, double b){
		return a + b;
	}
	double sub(double a, double b){
		return a - b;
	}
	double multi(double a, double b){
		return a * b;
	}	
	double div(double a, double b){
		return a / b;
	}
	boolean greater(double a, double b){
		return a > b;
	}
}
